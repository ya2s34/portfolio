    <h3 id="boot" class="tt_boot">BOOTSTRAP</h3>
    <div class="boot">

        <p>
            Bootstrap est un framework HTML, CSS et JavaScript gratuit pour le développement de sites web et d'applications mobiles. Il a été créé par Twitter et publié en 2011.<br>
            Il utilise un système de grille en CSS pour la mise en page responsive. Il est basé sur une grille de 12 colonnes, où chaque élément enfant peut être défini pour occuper un certain nombre de colonnes (allant de 1 à 12). Les classes CSS telles que "col-sm-4" ou "col-lg-6" peuvent être utilisées pour définir la largeur d'un élément enfant en fonction de la taille de l'écran (par exemple, "sm" signifie écran de petite taille et "lg" signifie écran large).

            Bootstrap utilise également des classes pour définir les positions d'un élément enfant par rapport aux autres (par exemple, "offset-sm-3" pour décaler un élément de 3 colonnes sur un écran de petite taille).

            Cela signifie que vous pouvez créer des mises en page flexibles qui s'adaptent à différentes tailles d'écran sans avoir à écrire beaucoup de code CSS personnalisé. Le système de grille de Bootstrap est très populaire et largement utilisé pour la mise en page de sites web et d'applications.</p>

